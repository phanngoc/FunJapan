        <p>Hokkaido yang indah yang terletak di pulau paling utara pulau Jepang, dan jika dibandingkan dengan banyak bagian lain dari negara ini yang sedikit berkembang dan masih kurang pengunjung. Hokkaido dikenal oleh wisatawan domestik &nbsp;menjadi
            tempat untuk didatangi di musim dingin, dan meskipun musim bisa keras di sini, salah satunya hujan salju yang turun dengan deras. Jika kamu suka ski atau snowboarding, maka Hokkaido sulit untuk dikalahkan sebagai tujuan ski premium,
            dan kamu juga dapat menikmati pemandangan romantis seperti lautan beku dan salju gunung berapi.<br>
            <br> Pada bulan-bulan musim panas, Hokkaido hangat dan menyenangkan, meskipun suhu yang lebih lembab daripada pulau-pulau lain dari Jepang. Terlepas dari kenyataan bahwa i namanya sebagai tempat olahraga musim dingin, Hokkaido memiliki
            banyak hal yang bisa dilakukan pada musim panas, dan siapa saja yang ingin melakukan aktivitas di luar akan kamu akan mendapatkan beberapa pilihan kegiatan seperti bersepeda dan hiking.</p>
        <p>Tergantung pada preferensi kamu, ada sesuatu untuk semua orang lihat dan dilakukan di sini, mulai dari kota-kota yang besar sampai kota-kota kuno.<br>
            <br> Berikut adalah 5 tempat terbaikuntuk dikunjungi di Hokkaido.<br>
            <br>
            <span style="text-decoration: underline;">Sapporo:</span></p>
        <div>
            <span style="text-decoration: underline;">
                <img height="530" alt="20161128-15-01-Hokkaido" width="850" src="https://malaysia.fun-japan.jp/~/media/Mpf/Articles/2017/04/13/TH_20170413-17-02-bus-in-japan.ashx?la=en"><br>
            </span>
        </div>
        <div></div>
        <p>Sapporo adalah ibu kota Hokkaido dan juga kota terbesar. Dikenal baru-baru ini di kalangan orang Jepang, yang telah didirikan pada abad ke-19, dan hal ini sangat terkenal karena taman yang rindang dan bir terkenal.</p>
        Dengan demikian, kunjungan di sini tidak akan lengkap tanpa perjalanan ke Museum Bir Sapporo, di mana kamu dapat mempelajari semua tentang sejarah pembuatan bir di Jepang, dan mendengar kisah terkenal Sapporo Brewing Company. Museum ini memungkinkan untuk
        ikut tur pabrik bir dan berakhir di ruang mencicipi di mana kamu dapat mencicipi beberapa brews terbaik yang ditawarkan.<br>
        <br> Jika kamu sedang mood ingin pergi ke suatu tempat sedikit lebih tenang, maka jangan lewatkan kesempatan untuk mengunjungi taman paling terkenal di Sapporo, Taman Odori, yang merupakan salah satu tempat yang paling ikonik di kota.
        Taman membentang sepanjang 15 blok dan ditutupi rumput dan air mancur yang cantik.
